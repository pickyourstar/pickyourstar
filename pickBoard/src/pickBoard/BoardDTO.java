package pickBoard;

public class BoardDTO {
	
	//star_number 를 string 에서 int 로 변경
	private int STAR_NUMBER;
	private String STAR_TITLE, STAR_CONTENT, STAR_IMAGE;
	private int STAR_COUNT, STAR_REPLY;
	private String STAR_WRITE, STAR_MODIFY, STAR_DELETE;
	private String WRITER;
	
	
	//getter / setter 구성
	public int getSTAR_NUMBER() {
		return STAR_NUMBER;
	}
	public void setSTAR_NUMBER(int sTAR_NUMBER) {
		STAR_NUMBER = sTAR_NUMBER;
	}
	public String getSTAR_TITLE() {
		return STAR_TITLE;
	}
	public void setSTAR_TITLE(String sTAR_TITLE) {
		STAR_TITLE = sTAR_TITLE;
	}
	public String getSTAR_CONTENT() {
		return STAR_CONTENT;
	}
	public void setSTAR_CONTENT(String sTAR_CONTENT) {
		STAR_CONTENT = sTAR_CONTENT;
	}
	public String getSTAR_IMAGE() {
		return STAR_IMAGE;
	}
	public void setSTAR_IMAGE(String sTAR_IMAGE) {
		STAR_IMAGE = sTAR_IMAGE;
	}
	public int getSTAR_COUNT() {
		return STAR_COUNT;
	}
	public void setSTAR_COUNT(int sTAR_COUNT) {
		STAR_COUNT = sTAR_COUNT;
	}
	public int getSTAR_REPLY() {
		return STAR_REPLY;
	}
	public void setSTAR_REPLY(int sTAR_REPLY) {
		STAR_REPLY = sTAR_REPLY;
	}
	public String getSTAR_WRITE() {
		return STAR_WRITE;
	}
	public void setSTAR_WRITE(String sTAR_WRITE) {
		STAR_WRITE = sTAR_WRITE;
	}
	public String getSTAR_MODIFY() {
		return STAR_MODIFY;
	}
	public void setSTAR_MODIFY(String sTAR_MODIFY) {
		STAR_MODIFY = sTAR_MODIFY;
	}
	public String getSTAR_DELETE() {
		return STAR_DELETE;
	}
	public void setSTAR_DELETE(String sTAR_DELETE) {
		STAR_DELETE = sTAR_DELETE;
	}
	public String getWRITER() {
		return WRITER;
	}
	public void setWRITER(String wRITER) {
		WRITER = wRITER;
	}
	
	
	
	

}
